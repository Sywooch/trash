	/**
	 * @param string $table
	 * @param string $where
	 * @param array $params
	 *
	 * @return int
	 * @throws CException
	 */
	private function geItemId($table, $where, $params)
	{
		$command = $this->getDbConnection()->
			createCommand('SELECT id FROM ' . $table .' WHERE ' . $where)->
			bindValues($params);

		$id = intval($command->queryScalar());
		if ($id < 1) {
			throw new CException('Не найден id для ' . $table);
		}

		return $id;
	}
